class TestPep563LocalDataclassResolved:
    """Happy-path counterpart of ``TestDataclassUnresolvableLocalField``: when
    a function is defined under ``from __future__ import annotations`` and
    references a *local* dataclass that's actually visible in the enclosing
    closure, ``_resolve_annotations``'s fallback path (the one that combines
    ``fn.__globals__`` with ``inspect.getclosurevars(fn).nonlocals``) must
    succeed and the op must register & run normally.

    Why this needs ``exec`` to set up: the test file as a whole does NOT
    enable ``from __future__ import annotations`` (that would silently
    string-ify every other test's annotations, a high-blast-radius change).
    We therefore compile a small standalone snippet WITH that future flag
    and inject a local frozen dataclass into its exec namespace. This
    reproduces the real-world bug class -- "user wrote PEP 563 + factory
    function returning an op" -- without contaminating the rest of the file.

    Without this test the resolver fallback (``_resolve_annotations``,
    namespace-merging branch) has no regression net: dropping
    ``cv.nonlocals`` from the merge would still leave every other test
    green.
    """

    def test_local_dataclass_resolved_via_closure_fallback(self):
        import dataclasses
        import textwrap

        # Step 1: build a frozen dataclass that will live ONLY in the exec
        # namespace -- it is intentionally NOT importable from any module,
        # so ``typing.get_type_hints`` (which can only see module globals)
        # MUST fail and we MUST fall through to the closurevars-based path.
        @dataclasses.dataclass(frozen=True)
        class _LocalCfg:
            scale: float
            bias: float

        # Step 2: source for a factory function whose annotations get
        # stringified by ``from __future__ import annotations``. ``_LocalCfg``
        # is referenced both in the annotation (as a string) AND in the
        # function body (as a real call) -- the body reference is what makes
        # ``inspect.getclosurevars`` report ``_LocalCfg`` in ``.nonlocals``,
        # which is exactly the namespace lookup we want to exercise.
        src = textwrap.dedent(
            """
            from __future__ import annotations

            def make_op(register, name, _LocalCfg):
                @register(name=name)
                def _op(x: torch.Tensor, cfg: _LocalCfg) -> torch.Tensor:
                    # body reference forces _LocalCfg into cv.nonlocals
                    _ = _LocalCfg
                    return x * cfg.scale + cfg.bias
                return _op
            """
        )

        ns: dict = {"torch": torch}
        exec(compile(src, "<pep563-local-dataclass>", "exec"), ns)

        op = ns["make_op"](magi_register_custom_op, "test::dc_pep563_local_resolved", _LocalCfg)

        x = torch.tensor([1.0, 2.0, 3.0])
        out = op(x, _LocalCfg(scale=2.0, bias=1.0))
        assert_close(out, torch.tensor([3.0, 5.0, 7.0]))


